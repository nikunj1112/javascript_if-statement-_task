// Age Group Classification

var age = 6;
{
    if (age < 18) {
        console.log("Minor"); // Below 18

    } else if (age >= 18 && age <= 60) {
        console.log("Adult"); // Between 18 and 60 

    } else if (age > 60) {
        console.log("Senior Citizen");  // Above 60

    } else {
        console.log("Invalid Age");

    }

}